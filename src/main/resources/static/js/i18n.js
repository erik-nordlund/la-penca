function getLanguage() {
    return localStorage.getItem("language") || "en";
}

function setLanguage(lang) {
    localStorage.setItem("language", lang);
    applyLanguage();

    if (typeof updateLanguageButtons === "function") {
        updateLanguageButtons();
    }
}

function t(key) {
    const lang = getLanguage();
    return translations[lang]?.[key] || translations.en[key] || key;
}
function translateTeam(teamName) {
    const lang = getLanguage();

    return translations[lang]?.teams?.[teamName]
        || translations.en?.teams?.[teamName]
        || teamName;
}

function applyLanguage() {
    const lang = getLanguage();
    const language = translations[lang] || translations.en;

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");

        if (language[key]) {
            el.innerText = language[key];
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");

        if (language[key]) {
            el.placeholder = language[key];
        }
    });
}